------------------------------------------------------------------------------------------------------------------------
--	EXTERNAL SCRIPTS AND APIS
------------------------------------------------------------------------------------------------------------------------

local KickAPI = require(script:GetCustomProperty("API_Kick"))

------------------------------------------------------------------------------------------------------------------------
--	LOCAL FUNCTIONS
------------------------------------------------------------------------------------------------------------------------

local function OnGetBanReason(player)
	Events.BroadcastToPlayer(player, "UpdateReason", KickAPI.GetReason(player))
end

------------------------------------------------------------------------------------------------------------------------
--	INITIALIZATION
------------------------------------------------------------------------------------------------------------------------

Events.ConnectForPlayer("GetReason", OnGetBanReason)