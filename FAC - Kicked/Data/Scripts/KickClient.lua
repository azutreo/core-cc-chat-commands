local ReasonText = script:GetCustomProperty("Reason"):WaitForObject()

------------------------------------------------------------------------------------------------------------------------
-- LOCAL FUNCTIONS
------------------------------------------------------------------------------------------------------------------------

local function OnUpdateReason(reason)
	print(reason)
	ReasonText.text = reason
end

------------------------------------------------------------------------------------------------------------------------
-- INITIALIZATION
------------------------------------------------------------------------------------------------------------------------

Events.Connect("UpdateReason", OnUpdateReason)

Events.BroadcastToServer("GetReason")