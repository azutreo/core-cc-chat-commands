------------------------------------------------------------------------------------------------------------------------
--	EXTERNAL SCRIPTS AND APIS
------------------------------------------------------------------------------------------------------------------------

local BanAPI = require(script:GetCustomProperty("API_Bans"))

------------------------------------------------------------------------------------------------------------------------
--	LOCAL FUNCTIONS
------------------------------------------------------------------------------------------------------------------------

local function OnGetBanReason(player)
	Events.BroadcastToPlayer(player, "Ban_UpdateReason", BanAPI.GetBanReason(player))
end

------------------------------------------------------------------------------------------------------------------------
--	INITIALIZATION
------------------------------------------------------------------------------------------------------------------------

Events.ConnectForPlayer("Ban_GetReason", OnGetBanReason)